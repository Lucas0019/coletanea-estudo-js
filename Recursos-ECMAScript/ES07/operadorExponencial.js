//2^3 = 2 x 2 x 2 = 8

//Modo antigo
console.log(Math.pow(2, 3)); // 8

//Novo modo
console.log(2 ** 3);//8


//Mais exemplos

console.log(3 ** 4);
// Resultado: 81

console.log(10 ** -2);
// Resultado: 0.01

console.log(2 ** 3 ** 2);
// Resultado: 512

console.log((2 ** 3) ** 2);
// Resultado: 64